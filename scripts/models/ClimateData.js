/**
 * Represents climate configuration for a region.
 * This is a de-minified version of the original `f` class.
 */
export class ClimateData {
  constructor() {
    this.name = null;
    this.isVolcanic = false;
    this.humidity = 0;
    this.baseTemperature = 0;
    this.temperatureRange = { max: 0, min: 0 };
  }
}

